#!/usr/bin/env python
"""
Cloud-side training entry point.
=================================

Generated by DataBuilder's remote-training-bundle feature. Reads the
snapshotted ``training_config.json``, opens the prebuilt cache, and
runs training to completion. Outputs land in ``./output/``.

Usage::

    python train.py

Environment knobs:
    HF_TOKEN              required if the base model is a gated HF repo
                          (Flux dev, SD3.x). Skipped when the bundle
                          already contains a ./model/ directory.
    DATABUILDER_DEVICE    override device selection (default: auto)
    DATABUILDER_LOG       set to "DEBUG" for verbose logs

This file expects to live at the bundle root, alongside ``cache/``,
``config/``, ``dataset_sorter/``, and (optionally) ``model/``.
"""

from __future__ import annotations

import dataclasses
import json
import logging
import os
import sys
import time
from pathlib import Path

HERE = Path(__file__).resolve().parent

# Vendored DataBuilder source MUST be importable before any of its
# modules are touched. We sys.path-prepend HERE so `dataset_sorter`
# resolves to the bundled copy rather than a system-installed one.
sys.path.insert(0, str(HERE))

# Configure logging early so the heavy imports below produce useful
# diagnostics.
logging.basicConfig(
    level=os.environ.get("DATABUILDER_LOG", "INFO"),
    format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
    stream=sys.stdout,
)
log = logging.getLogger("databuilder.remote_train")


def _load_config() -> tuple["dataset_sorter.models.TrainingConfig", str]:
    """Load training config and model path from the bundle snapshot.

    Returns (config, model_path).  model_path is stored in the JSON but
    is NOT a TrainingConfig field — it's passed separately to the Trainer.
    """
    cfg_path = HERE / "config" / "training_config.json"
    if not cfg_path.is_file():
        raise FileNotFoundError(f"Missing {cfg_path}")
    raw = json.loads(cfg_path.read_text(encoding="utf-8"))

    # ── Bundle-side path overrides ────────────────────────────────────
    # If the user bundled a local model, point at the local copy.
    bundled_model = HERE / "model"
    if bundled_model.is_dir():
        raw["model_path"] = str(bundled_model)
        log.info("Using bundled model directory: %s", bundled_model)
    elif (HERE / "model").exists():
        files = list((HERE / "model").iterdir())
        single_files = [f for f in files
                         if f.suffix.lower() in (".safetensors", ".ckpt", ".pt", ".bin")]
        if single_files:
            raw["model_path"] = str(single_files[0])
            log.info("Using bundled single-file model: %s", single_files[0])

    model_path = raw.pop("model_path", "")

    raw["mmap_prebuilt_cache_dir"] = str(HERE / "cache")
    raw["output_dir"] = str(HERE / "output")
    raw["pipeline_integration"] = False
    raw["mmap_dataset"] = False

    from dataset_sorter.models import TrainingConfig
    field_names = {f.name for f in dataclasses.fields(TrainingConfig)}
    clean = {k: v for k, v in raw.items()
             if k in field_names and not k.startswith("_")}
    return TrainingConfig(**clean), model_path


def _progress(current: int, total: int, message: str) -> None:
    """Print a progress line. Cloud users want stdout to be tail-friendly."""
    if total > 0:
        print(f"[{current}/{total}] {message}", flush=True)
    else:
        print(message, flush=True)


def _on_step(step: int, loss: float, lr: float) -> None:
    # Throttle to every ~30 steps so the log isn't a wall.
    if step % 30 == 0:
        print(f"  step={step} loss={loss:.5f} lr={lr:.2e}", flush=True)


def main() -> int:
    t0 = time.time()
    log.info("DataBuilder remote training launcher starting")
    log.info("Bundle root: %s", HERE)

    config, model_path = _load_config()
    log.info(
        "Config: model_type=%s model_path=%s epochs=%d batch=%d resolution=%d",
        config.model_type, model_path,
        config.epochs, config.batch_size, config.resolution,
    )

    cache_dir = Path(config.mmap_prebuilt_cache_dir)
    manifest = json.loads(
        (cache_dir / "manifest.json").read_text(encoding="utf-8")
    )
    log.info(
        "Cache: %d samples, model_type=%s, dtype=%s, encoders=%s",
        manifest["num_samples"], manifest["model_type"],
        manifest.get("dtype"), manifest.get("te_components"),
    )

    output_dir = Path(config.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    from dataset_sorter.trainer import Trainer
    trainer = Trainer(config)
    trainer.setup_with_prebuilt_cache(
        model_path=model_path,
        cache_dir=cache_dir,
        output_dir=output_dir,
        progress_fn=_progress,
    )

    log.info("Starting training: %d steps total", trainer.total_steps)
    try:
        trainer.train(progress_fn=_progress, loss_fn=_on_step, sample_fn=None)
    except KeyboardInterrupt:
        log.warning("Interrupted by user — saving current state…")
        try:
            trainer._save_checkpoint("interrupted")
        except Exception as e:
            log.error("Save on interrupt failed: %s", e)
        return 130
    finally:
        try:
            trainer.cleanup()
        except Exception:
            pass

    elapsed = time.time() - t0
    log.info("Training finished in %.0f s. Outputs at %s", elapsed, output_dir)
    log.info("Pull results back with: rsync -avz <this-host>:%s/ ./", output_dir)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
