"""
Module: bug_reporter.py
========================
Automatic bug-report helper for DataBuilder.

When a critical error occurs the user is offered a one-click way to open a
pre-filled GitHub issue so they can submit a bug report without having to
manually copy stack traces.

Public API:
    - format_bug_report(error, context)     — build a markdown issue body string
    - get_copyable_report(error, context)   — same, for clipboard copy
    - open_github_issue(error, context)     — open browser with pre-filled issue
    - show_bug_report_dialog(error, context, parent) — Qt dialog (Copy + Report + Dismiss)

The Qt dialog is imported lazily so this module can be used from CLI/headless
contexts without PyQt6 being installed.
"""

import logging
import platform
import sys
import traceback
import urllib.parse
import webbrowser

log = logging.getLogger(__name__)

GITHUB_ISSUES_URL = "https://github.com/marmotte5/DataBuilder-/issues/new"

# Labels applied to auto-generated issues (GitHub labels must already exist)
_AUTO_LABELS = "bug,auto-reported"


def _databuilder_version() -> str:
    """Return DataBuilder version, falling back gracefully if unavailable."""
    try:
        from importlib.metadata import version
        return version("dataset-sorter")
    except Exception as e:
        log.debug("Could not read DataBuilder version: %s", e)
        return "unknown (check git log)"


def format_bug_report(error: BaseException | None = None, context: str = "") -> str:
    """Format error details into a GitHub issue body (Markdown).

    Args:
        error: The exception to report. If None, formats a generic report.
        context: Human-readable string describing what the user was doing.

    Returns:
        Markdown string ready to paste into a GitHub issue.
    """
    if error is not None:
        error_line = f"`{type(error).__name__}: {error}`"
        tb_text = "".join(traceback.format_exception(type(error), error, error.__traceback__))
    else:
        error_line = "_No exception object available_"
        tb_text = traceback.format_exc() or "_No traceback available_"

    try:
        import torch
        torch_ver = torch.__version__
        cuda_ver = torch.version.cuda or "N/A"
        device = "CUDA" if torch.cuda.is_available() else ("MPS" if torch.backends.mps.is_available() else "CPU")
    except ImportError:
        torch_ver = cuda_ver = device = "torch not installed"

    return f"""## Bug Report (auto-generated by DataBuilder)

**Error:** {error_line}

**Context:** {context or "_Not provided_"}

**Traceback:**
```
{tb_text.strip()}
```

**System Information:**
| Field | Value |
|-------|-------|
| OS | {platform.system()} {platform.release()} ({platform.machine()}) |
| Python | {sys.version.split()[0]} |
| DataBuilder | {_databuilder_version()} |
| PyTorch | {torch_ver} |
| CUDA | {cuda_ver} |
| Compute device | {device} |

**Steps to reproduce:**
1. _(Please describe what you were doing when this error occurred)_

**Additional context:**
_(Any other details that might help — dataset size, model type, etc.)_
"""


def get_copyable_report(error: BaseException | None = None, context: str = "") -> str:
    """Return a formatted bug report string the user can copy-paste into a GitHub issue."""
    return format_bug_report(error, context)


def open_github_issue(
    error: BaseException | None = None,
    context: str = "",
    title_prefix: str = "[Bug]",
) -> None:
    """Open the default browser with a pre-filled GitHub issue form.

    Args:
        error: The exception to report.
        context: Human-readable context string.
        title_prefix: Prefix for the issue title.
    """
    body = format_bug_report(error, context)
    if error is not None:
        short_msg = str(error)[:80].replace("\n", " ")
        title = f"{title_prefix} {type(error).__name__}: {short_msg}"
    else:
        title = f"{title_prefix} Error in DataBuilder"

    params = urllib.parse.urlencode({
        "title": title,
        "body": body,
        "labels": _AUTO_LABELS,
    })
    url = f"{GITHUB_ISSUES_URL}?{params}"
    log.info("Opening bug report URL: %s", url[:120])
    webbrowser.open(url)


def show_bug_report_dialog(
    error: BaseException | None = None,
    context: str = "",
    parent=None,
) -> None:
    """Show a PyQt6 dialog offering Copy-to-clipboard and Report-on-GitHub.

    This is a modal dialog with three buttons:
      - "Copy Report" — copies the formatted Markdown to the clipboard
      - "Report on GitHub" — opens the browser with a pre-filled issue
      - "Dismiss" — closes without action

    Falls back silently if PyQt6 is not available (headless / CLI use).

    Args:
        error: The exception to report.
        context: Human-readable context string shown in the issue body.
        parent: Optional parent QWidget.
    """
    try:
        from PyQt6.QtWidgets import (
            QDialog, QVBoxLayout, QHBoxLayout, QLabel,
            QTextEdit, QPushButton, QApplication,
        )
        from PyQt6.QtCore import Qt
    except ImportError:
        # Headless environment — just log and open browser directly
        log.warning("PyQt6 unavailable — opening bug report in browser directly")
        open_github_issue(error, context)
        return

    try:
        report_text = format_bug_report(error, context)

        dialog = QDialog(parent)
        dialog.setWindowTitle("DataBuilder — Report a Bug")
        dialog.setMinimumWidth(640)
        dialog.setMinimumHeight(480)
        dialog.setAttribute(Qt.WidgetAttribute.WA_DeleteOnClose)

        layout = QVBoxLayout(dialog)
        layout.setSpacing(12)

        # Header
        if error is not None:
            header = QLabel(
                f"<b>{type(error).__name__}</b>: {str(error)[:120]}"
            )
        else:
            header = QLabel("<b>An error occurred.</b>")
        header.setWordWrap(True)
        layout.addWidget(header)

        info = QLabel(
            "You can copy this report and paste it into a GitHub issue, "
            "or click <b>Report on GitHub</b> to open a pre-filled form in your browser."
        )
        info.setWordWrap(True)
        layout.addWidget(info)

        # Report preview
        text_edit = QTextEdit()
        text_edit.setPlainText(report_text)
        text_edit.setReadOnly(True)
        text_edit.setMinimumHeight(280)
        layout.addWidget(text_edit)

        # Buttons
        btn_row = QHBoxLayout()
        btn_row.addStretch()

        btn_copy = QPushButton("Copy Report")
        btn_github = QPushButton("Report on GitHub")
        btn_dismiss = QPushButton("Dismiss")

        btn_copy.setToolTip("Copy the full bug report to the clipboard")
        btn_github.setToolTip("Open a pre-filled GitHub issue in your browser")
        btn_dismiss.setToolTip("Close this dialog without reporting")

        btn_github.setDefault(True)

        btn_row.addWidget(btn_copy)
        btn_row.addWidget(btn_github)
        btn_row.addWidget(btn_dismiss)
        layout.addLayout(btn_row)

        def _copy():
            clipboard = QApplication.clipboard()
            if clipboard:
                clipboard.setText(report_text)
            btn_copy.setText("Copied!")
            btn_copy.setEnabled(False)

        def _report():
            open_github_issue(error, context)
            btn_github.setText("Opened in browser")
            btn_github.setEnabled(False)

        btn_copy.clicked.connect(_copy)
        btn_github.clicked.connect(_report)
        btn_dismiss.clicked.connect(dialog.accept)

        dialog.exec()

    except Exception:
        # If the dialog itself fails, open the browser as last resort
        log.exception("Bug report dialog failed — falling back to browser")
        try:
            open_github_issue(error, context)
        except Exception:
            pass
